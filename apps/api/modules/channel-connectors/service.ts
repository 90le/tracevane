const execFileAsync = promisify(execFile);
const CC_BRIDGE_ACTIONS: readonly CcBridgeServiceAction[] = [
  "preview",
  "install",
  "ensure-running",
  "start",
  "stop",
  "restart",
  "status",
];
const LOG_MAX_SIZE_BYTES = 10 * 1024 * 1024;

export type CcBridgeServiceCommandRunner = (
  command: CcBridgeServiceCommand,
) => Promise<CcBridgeServiceCommandResult>;

export type CcBridgeBinaryResolver = (command: string) => string | null;

