interface CcBridgePaths {
  rootDir: string;
  configPath: string;
  stateDir: string;
  logDir: string;
  logFile: string;
  runtimeFile: string;
}

function normalizeHomeDir(value: string | undefined): string {
  const trimmed = String(value || "").trim();
  return trimmed || os.homedir();
}

export function resolveChannelConnectorsPaths(
  config: StudioServerConfig,
): CcBridgePaths {
  const rootDir = path.join(config.openclawRoot, "studio", "channel-connectors", "cc-bridge");
  const logDir = path.join(rootDir, "logs");
  return {
    rootDir,
    configPath: path.join(rootDir, "config.toml"),
    stateDir: path.join(rootDir, "state"),
    logDir,
    logFile: path.join(logDir, "cc-bridge.log"),
    runtimeFile: path.join(rootDir, "runtime.json"),
  };
}

function readTextIfExists(filePath: string): string | null {
  try {
    return fs.readFileSync(filePath, "utf8");
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === "ENOENT") return null;
    throw error;
  }
}

function ensureParentDir(filePath: string): void {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function writeTextAtomic(filePath: string, content: string): void {
  ensureParentDir(filePath);
  const tempPath = `${filePath}.tmp-${process.pid}-${Date.now()}`;
  fs.writeFileSync(tempPath, content, "utf8");
  fs.renameSync(tempPath, filePath);
}

function normalizeServiceAction(value: unknown): CcBridgeServiceAction {
  return CC_BRIDGE_ACTIONS.includes(value as CcBridgeServiceAction)
    ? value as CcBridgeServiceAction
    : "preview";
}

function escapeTomlString(value: string): string {
  return JSON.stringify(value);
}

function quoteSystemdArg(value: string): string {
  return `"${value.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function quoteLaunchdString(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function gatewayEndpoint(): string {
  return "http://127.0.0.1:18796/v1";
}

